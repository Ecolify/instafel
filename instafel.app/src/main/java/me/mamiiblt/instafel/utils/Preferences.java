package me.mamiiblt.instafel.utils;

public class Preferences {
    String id, name, type;

    public Preferences(String id, String name, String type) {
        this.id = id;
        this.name = name;
        this.type = type;
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getType() {
        return type;
    }
}
